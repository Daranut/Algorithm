//โดยนาย A สามารถเดินแต่ละก้าวด้วยความยาว 1, 2, 3, 4, 5 
//จงหาว่านาย A สามารถเดินไปบ้านเพื่อนได้น้อยที่สุดกี่ก้าว (โจทย์จะให้ Input มาตัวนึงคือค่า x เป็นจำนวนเต็มบวกที่มากกว่า 0)

// Example Testcases
// minStep(14) จะตอบ 3 ก้าว (5 + 5 + 4)
// minStep(3) จะตอบ 1 ก้าว
function minStep(x) {
    // ใช้วิธีทางคณิตศาสตร์เพื่อหาจำนวนก้าวที่น้อยที่สุด
    // โดยให้ก้าวสุดท้ายมีความยาวเท่ากับ x หาร 5 ถ้า x ไม่หาร 5 ลงตัว
    // ถ้า x หาร 5 ลงตัว ให้ใช้ x หาร 5 เลย
    return Math.floor((x + 4) / 5);
  }
  
  // Example Testcases
  console.log(minStep(14)); // ผลลัพธ์ควรเป็น 3
  console.log(minStep(3));  // ผลลัพธ์ควรเป็น 1
  